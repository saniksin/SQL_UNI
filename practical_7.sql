-- У ході виконання практичної роботи було відпрацьовано основні
-- конструкції повторного використання SQL-коду на схемі publishing:
-- вкладені запити з NOT EXISTS (Задача 1), скалярні підзапити у
-- секції HAVING (Задача 2), загальні табличні вирази CTE разом з
-- віконними функціями (Задача 3) та VIEW як збережене віртуальне
-- подання (Задача 4).
--
-- Перевага вкладених запитів і подань над простими SELECT-виразами
-- полягає в тому, що складна логіка декомпозується на самостійні
-- частини: підзапит описує локальну умову, CTE — проміжний набір
-- даних, VIEW — повторно використовуваний фрагмент. Це робить запити
-- читабельнішими, спрощує підтримку (формули обчислюються в одному
-- місці) та дозволяє будувати багатошарову аналітику без копіювання
-- однакових SELECT.
--
-- На прикладі бази publishing продемонстровано бізнес-сенс цих
-- конструкцій: NOT EXISTS — пошук авторів без продажів; підзапит у
-- HAVING — динамічний поріг «вище середнього»; CTE з RANK() —
-- рейтинги книг у межах жанру; VIEW v_book_sales — централізоване
-- джерело даних для звітів про виручку.
