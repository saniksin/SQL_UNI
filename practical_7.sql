USE publishing;

-- ================================================================
-- Задача 1. Підзапит для визначення авторів, чиї книги не замовляли
-- (NOT EXISTS)
-- ================================================================
-- Логіка: для кожного автора підзапит шукає хоча б один зв'язок
-- AuthorBook + OrderItem (тобто книгу автора, яка з'явилась у позиціях
-- замовлень). NOT EXISTS залишає лише тих авторів, для яких підзапит
-- порожній — їхні книги жодного разу не замовляли.
-- Результат: перелік авторів без жодного продажу.

SELECT a.AuthorID, a.Name
  FROM Authors a
 WHERE NOT EXISTS (
         SELECT 1
           FROM AuthorBook ab
           JOIN OrderItem  oi ON oi.BookID = ab.BookID
          WHERE ab.AuthorID = a.AuthorID
       );

-- ---- Висновок студента (Задача 1) ----
-- У ході виконання завдання був складений вкладений запит з конструкцією
-- NOT EXISTS, який повертає авторів, чиї книги жодного разу не з'являлися
-- у позиціях замовлень (OrderItem). Підзапит для кожного автора перевіряє
-- наявність зв'язку AuthorBook ↔ OrderItem, а зовнішній SELECT залишає
-- тільки тих, для кого підзапит порожній. Це типовий аналітичний зріз
-- «хто не приносить виручки» — корисний для маркетингових рішень
-- видавництва. Перевага NOT EXISTS у порівнянні з LEFT JOIN ... IS NULL —
-- читабельність наміру: запит явно відповідає на запитання «чи існує
-- замовлення для книги цього автора?».

-- ================================================================
-- Задача 2. Книги з продажами вище середнього (HAVING + підзапит)
-- ================================================================
-- Логіка: зовнішній запит групує позиції замовлень по книзі, рахує
-- сумарну виручку Revenue = SUM(Quantity * UnitPrice). HAVING порівнює
-- цю виручку з результатом скалярного підзапиту, який обчислює середнє
-- значення по позиціях OrderItem. Залишаються лише книги-лідери з
-- виручкою понад середній рівень.
-- Результат: книги з продажами вище середнього.

SELECT b.Title,
       SUM(oi.Quantity * oi.UnitPrice) AS Revenue
  FROM OrderItem oi
  JOIN Books b ON b.BookID = oi.BookID
 GROUP BY b.Title
HAVING Revenue > (
         SELECT AVG(Quantity * UnitPrice)
           FROM OrderItem
       )
 ORDER BY Revenue DESC;

-- ---- Висновок студента (Задача 2) ----
-- У ході виконання завдання було побудовано вкладений запит з HAVING,
-- у якому фільтрація відбувається не по рядках вихідної таблиці, а по
-- агрегованих значеннях. Скалярний підзапит обчислює середню вартість
-- позиції замовлення, а зовнішній SELECT відсіює тільки ті книги, чия
-- сумарна виручка перевищує цей рівень. Така конструкція дозволяє в
-- одному запиті отримати «динамічний поріг» (середнє значення) і
-- порівняти з ним кожну групу — це гнучкіше за хардкодну константу і
-- автоматично адаптується при зміні даних.

-- ================================================================
-- Задача 3. Рейтинг книг у межах жанру (CTE + віконна функція RANK)
-- ================================================================
-- Логіка: CTE sales (через WITH ... AS) обчислює виручку по кожній книзі
-- разом з її жанром — це проміжна таблиця, яку зручно використовувати
-- у головному SELECT. Віконна функція RANK() OVER (PARTITION BY Genre
-- ORDER BY Revenue DESC) призначає кожній книзі місце в рейтингу її жанру:
-- 1 — лідер жанру, 2 — наступний за виручкою тощо.
-- Результат: список книг із виручкою та позицією у жанровому рейтингу.

WITH sales AS (
  SELECT b.Title, b.Genre,
         SUM(oi.Quantity * oi.UnitPrice) AS Revenue
    FROM Books b
    JOIN OrderItem oi ON oi.BookID = b.BookID
   GROUP BY b.Title, b.Genre
)
SELECT Title, Genre, Revenue,
       RANK() OVER (PARTITION BY Genre ORDER BY Revenue DESC) AS GenreRank
  FROM sales
 ORDER BY Genre, GenreRank;

-- ---- Висновок студента (Задача 3) ----
-- У ході виконання завдання було побудовано аналітичний запит з
-- використанням загального табличного виразу CTE (WITH ... AS) та
-- віконної функції RANK(). CTE винесено окремо проміжне обчислення
-- виручки по кожній книзі, що зробило основний запит коротшим і легшим
-- для читання — та сама логіка без CTE вимагала б дубльованого підзапиту
-- у FROM. Віконна функція RANK() з PARTITION BY Genre автоматично
-- ранжує книги в межах кожного жанру окремо — це типове рішення для
-- задач «топ-N у категорії». Перевагою CTE є читабельність складних
-- аналітичних запитів та можливість повторно посилатися на проміжний
-- набір даних в одному запиті.

-- ================================================================
-- Задача 4. Повторне використання коду — VIEW
-- ================================================================
-- Створюємо віртуальне подання v_book_sales, яке зберігає підрахунок
-- виручки по кожній книзі. LEFT JOIN потрібен, щоб у поданні з'явилися
-- й ті книги, які жодного разу не продавалися (Revenue буде NULL).
-- Після створення VIEW використовується як звичайна таблиця у будь-яких
-- наступних SELECT, ORDER BY, JOIN тощо.

CREATE OR REPLACE VIEW v_book_sales AS
SELECT b.BookID,
       b.Title,
       SUM(oi.Quantity * oi.UnitPrice) AS Revenue
  FROM Books b
  LEFT JOIN OrderItem oi ON oi.BookID = b.BookID
 GROUP BY b.BookID, b.Title;

-- Використання збереженого подання — звичайний SELECT, як з таблиці
SELECT *
  FROM v_book_sales
 ORDER BY Revenue DESC;

-- ---- Висновок студента (Задача 4) ----
-- У ході виконання завдання було створено віртуальне подання
-- v_book_sales, яке зберігає підрахунок сумарної виручки по кожній
-- книзі. Подання працює як збережений SELECT-вираз: у наступних запитах
-- до нього можна звертатися як до звичайної таблиці, не повторюючи
-- складну логіку з GROUP BY та JOIN. Це зменшує дублювання коду в
-- аналітичних запитах, спрощує підтримку (зміна формули виручки
-- виконується в одному місці — у визначенні VIEW) та робить код
-- зрозумілішим для інших розробників. LEFT JOIN у визначенні подання
-- забезпечує наявність у результаті всіх книг, навіть тих, які жодного
-- разу не продавалися — для них Revenue буде NULL.

