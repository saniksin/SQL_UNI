-- Мета: закріпити навички створення вкладених запитів (subqueries),
-- застосування конструкцій NOT EXISTS / IN / HAVING, CTE (WITH ... AS)
-- та VIEW для повторного використання коду в аналітичних вибірках
-- по схемі publishing.
--
-- У роботі відпрацьовуються:
--   - вкладений запит з NOT EXISTS (фільтр за відсутністю зв'язків);
--   - підзапит у HAVING (фільтр агрегату відносно середнього);
--   - CTE (WITH ... AS) разом з віконною функцією RANK();
--   - VIEW як спосіб повторно використати складний SELECT.
