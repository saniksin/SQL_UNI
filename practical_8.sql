USE publishing;

-- ================================================================
-- Задача 1. Робота з текстовими функціями
-- ================================================================
-- Логіка: чотири окремих запити демонструють основні рядкові функції
-- MySQL. UPPER приводить рядок до верхнього регістру, CONCAT склеює
-- кілька рядків в один, LIKE з шаблоном '%...%' шукає підрядок,
-- LENGTH повертає довжину рядка в байтах.
-- Результат: повні імена авторів у верхньому регістрі, e-mail-підписи
-- співробітників, співробітники з домена 'pub.ch' та довжина назв книг.

-- Повне ім'я автора у верхньому регістрі
SELECT UPPER(Name) AS AuthorNameUpper
  FROM Authors;

-- Формування електронного підпису працівника: "Ім'я <email>"
SELECT CONCAT(Name, ' <', Email, '>') AS Signature
  FROM Employees;

-- Пошук співробітників, чий email містить домен 'pub.ch'
SELECT Name, Email
  FROM Employees
 WHERE Email LIKE '%pub.ch%';

-- Визначення довжини назви книги
SELECT Title, LENGTH(Title) AS TitleLength
  FROM Books;

-- ---- Висновок студента (Задача 1) ----
-- У ході виконання завдання було відпрацьовано основні текстові функції
-- MySQL на даних схеми publishing. UPPER застосовано для приведення імен
-- авторів до верхнього регістру — типовий прийом для уніфікованого
-- виведення у звітах. CONCAT використано для формування e-mail-підпису
-- співробітника у форматі "Ім'я <email>" — це готовий до вставки рядок
-- для службового листування. LIKE з шаблоном '%pub.ch%' відфільтрував
-- співробітників за корпоративним доменом, демонструючи пошук підрядка
-- у текстовому полі. LENGTH повернула довжину назви кожної книги в
-- байтах — корисно для перевірки обмежень довжини при експорті в інші
-- системи. Текстові функції є зручним інструментом для форматування
-- та валідації рядкових даних безпосередньо на рівні запиту.

-- ================================================================
-- Задача 2. Робота з числовими функціями
-- ================================================================
-- Логіка: числові функції MySQL дозволяють виконувати математичні
-- перетворення прямо у SELECT. ROUND округлює дробове значення до
-- заданої кількості знаків після коми (тут — 2, для грошового формату),
-- AVG і SUM — стандартні агрегати, MOD повертає остачу від ділення
-- (для перевірки парності — MOD(x, 2) дає 0 для парних, 1 для непарних).
-- Результат: дохід по кожному замовленню, середня ціна книги в продажах
-- та позначення позицій з непарною кількістю.

-- Розрахунок загального доходу по кожному замовленню
SELECT OrderID,
       ROUND(SUM(Quantity * UnitPrice), 2) AS TotalRevenue
  FROM OrderItem
 GROUP BY OrderID;

-- Оцінка середньої ціни книги у продажах
SELECT ROUND(AVG(UnitPrice), 2) AS AvgBookPrice
  FROM OrderItem;

-- Визначення, які позиції замовлень мають непарну кількість
SELECT OrderItemID, Quantity, MOD(Quantity, 2) AS IsOdd
  FROM OrderItem;

-- ---- Висновок студента (Задача 2) ----
-- У ході виконання завдання було застосовано числові функції MySQL для
-- аналітичних обчислень над таблицею OrderItem. ROUND з другим аргументом
-- 2 округлила суму Quantity * UnitPrice до двох знаків після коми —
-- стандарт для грошових значень у звітах. Поєднання SUM з GROUP BY
-- OrderID дало загальну суму по кожному замовленню, а AVG(UnitPrice) —
-- середню ціну книги серед усіх проданих позицій. Функція MOD
-- продемонструвала свій типовий випадок використання — перевірку
-- парності значення (MOD(x, 2) повертає 0 для парних і 1 для непарних
-- чисел). Числові функції зручні тим, що дозволяють виконати математичну
-- обробку прямо у SELECT, без додаткової постобробки на стороні
-- застосунку.

-- ================================================================
-- Задача 3. Робота з часовими функціями
-- ================================================================
-- Логіка: CURDATE() повертає поточну дату сервера. DATEDIFF(d1, d2)
-- обчислює різницю між двома датами в днях — використовується для
-- фільтрації "старих" записів. YEAR і MONTH витягують відповідні
-- компоненти з типу DATE, що зручно для агрегації по періодах.
-- Результат: поточна дата, замовлення старші за 100 днів та
-- помісячно-річне представлення дат початку контрактів.

-- Поточна дата
SELECT CURDATE() AS Today;

-- Замовлення, які зроблено більше ніж 100 днів тому
SELECT OrderID,
       OrderDate,
       DATEDIFF(CURDATE(), OrderDate) AS DaysAgo
  FROM Orders
 WHERE DATEDIFF(CURDATE(), OrderDate) > 100;

-- Рік і місяць створення контракту
SELECT ContractID,
       YEAR(StartDate)  AS YearStart,
       MONTH(StartDate) AS MonthStart
  FROM Contracts;

-- ---- Висновок студента (Задача 3) ----
-- У ході виконання завдання було відпрацьовано основні часові функції
-- MySQL. CURDATE() повертає поточну дату сервера у форматі YYYY-MM-DD —
-- зручно для прив'язки звітів до моменту виконання запиту. DATEDIFF
-- обчислила різницю в днях між поточною датою та датою замовлення, що
-- дозволило відфільтрувати замовлення давністю понад 100 днів — типовий
-- сценарій для звітів про "застарілі" або "довгі" угоди. YEAR і MONTH
-- витягли компоненти року та місяця з поля StartDate контрактів, що є
-- основою для побудови помісячних і річних агрегатів. Часові функції
-- є ключовим інструментом для аналітики за часовими інтервалами без
-- необхідності зберігати рік і місяць окремими полями.

-- ================================================================
-- Задача 4. Логічні та умовні функції
-- ================================================================
-- Логіка: IF(умова, value_true, value_false) — тернарний оператор для
-- двох варіантів значення. CASE WHEN ... THEN ... ELSE ... END — більш
-- гнучка форма для трьох і більше гілок: умови перевіряються зверху
-- вниз, ELSE спрацьовує, якщо жодна умова не виконана.
-- Результат: статус контракту (Active/Closed) та категорія книги за
-- роком видання (нові видання / сучасні / архів).

-- Визначити статус контракту
SELECT ContractID,
       IF(EndDate IS NULL, 'Active', 'Closed') AS ContractStatus
  FROM Contracts;

-- Категоризація книг за роком видання
SELECT Title,
       PublishYear,
       CASE
         WHEN PublishYear >= 2025                  THEN 'Нові видання'
         WHEN PublishYear BETWEEN 2020 AND 2024    THEN 'Сучасні'
         ELSE 'Архів'
       END AS Category
  FROM Books;

-- ---- Висновок студента (Задача 4) ----
-- У ході виконання завдання було застосовано логічні (умовні) функції
-- MySQL для формування похідних значень безпосередньо у SELECT. Функція
-- IF використана у задачі визначення статусу контракту: перевіряється,
-- чи є EndDate NULL — якщо так, контракт активний, інакше закритий. Для
-- категоризації книг застосовано конструкцію CASE WHEN ... THEN ... END
-- з трьома гілками: нові видання (>= 2025), сучасні (2020–2024) та архів
-- (всі інші). На відміну від IF, CASE дозволяє описати довільну кількість
-- варіантів і є основним інструментом для формування довідкових міток
-- у звітах. Обидві функції корисні тим, що переносять бізнес-логіку
-- класифікації безпосередньо у запит, спрощуючи звітні застосунки.

-- ================================================================
-- Задача 5. Службові функції
-- ================================================================
-- Логіка: IFNULL(expr, replacement) повертає expr, якщо він не NULL,
-- або replacement у протилежному випадку — використовується для
-- "красивого" виводу пропущених значень. Друга частина задачі поєднує
-- IF з LIKE для базової валідації e-mail (наявність символу '@').
-- Результат: телефон автора з підстановкою заглушки замість NULL та
-- позначення e-mail співробітника як валідного / невалідного.

-- Заміна NULL значень
SELECT Name,
       IFNULL(Phone, '— не вказано —') AS PhoneDisplay
  FROM Authors;

-- Порівняння даних
SELECT Name,
       Email,
       IF(Email LIKE '%@%', 'Valid email', 'Invalid email') AS CheckEmail
  FROM Employees;

-- ---- Висновок студента (Задача 5) ----
-- У ході виконання завдання було відпрацьовано службові функції MySQL,
-- призначені насамперед для роботи з NULL-значеннями та простої
-- валідації даних. IFNULL замінила NULL у полі Phone авторів на
-- зрозумілий заглушковий текст "— не вказано —", що покращує читабельність
-- звіту для кінцевого користувача. Поєднання IF з LIKE '%@%' дало просту
-- перевірку формальної коректності e-mail співробітника: наявність
-- символу '@' трактується як валідна адреса. Службові функції корисні
-- тим, що дозволяють централізовано опрацювати NULL та виконати базові
-- перевірки даних на рівні SQL — до того, як значення потрапить у бізнес-
-- логіку застосунку.

-- ================================================================
-- Загальний висновок
-- ================================================================
-- У ході виконання практичної роботи було відпрацьовано п'ять груп
-- вбудованих функцій MySQL на схемі publishing: текстові
-- (UPPER, CONCAT, LENGTH, LIKE) — Задача 1; числові (ROUND, SUM, AVG,
-- MOD) — Задача 2; часові (CURDATE, DATEDIFF, YEAR, MONTH) — Задача 3;
-- логічні / умовні (IF, CASE WHEN) — Задача 4; службові
-- (IFNULL) — Задача 5.
--
-- Перевага вбудованих функцій полягає в тому, що вся обробка
-- (форматування рядків, округлення, обчислення дат, класифікація за
-- умовами, заміна NULL) виконується безпосередньо на сервері БД у
-- межах одного SELECT — без передачі сирих даних у застосунок.
-- Це робить запити самодостатніми, скорочує обсяг переданих даних
-- і централізує бізнес-логіку у БД.
--
-- На прикладі бази publishing продемонстровано бізнес-сенс цих
-- функцій: уніфіковане виведення імен (UPPER), готові підписи
-- співробітників (CONCAT), грошові суми у звітах (ROUND), фільтр
-- за давністю замовлень (DATEDIFF), статус контрактів (IF),
-- категоризація каталогу (CASE) та коректне відображення
-- відсутнього телефону автора (IFNULL).
